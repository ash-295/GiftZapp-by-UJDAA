            <h2 class="mb-3">Surprise gift ideas.</h2>
            <p>Waiting for the perfect moment to shock someone with a gift? Well, that is the definition of surprise. Everyone likes a surprise. Imagine, when you reach your home, a candle lit cake is waiting in your dim-lit bedroom, and friends swarm you all of a sudden. Doesn’t that seem amazing? So take your time to plan a surprise for someone you love. Surprises are what strike us and remain vividly in our memories.</p>
            <p>Here are some of the surprise gifts that we're sure will blow away anyone.</p>
            <h2 class="mb-3 mt-5">#1. A pair of shoes inside a box of pipe fittings:</h2>
            <p>Sounds childish doesn't it?  If you gift someone, they are bound to recognize the gift from the box. Well we will use that against them. They'll recognize it to be a prank, but they won't be able to guess the actual gift (Unless they've read our article). When they open the box, they will be surprised again to find shoes.This trick will work wonders if you have a gift that would otherwise look dull and monotonous.
            </p>
            <p>So this will make your gift look many times better( as proved by psychology). This was actually tried by pilots. The pilots came to know that the flight was going to get delayed by an hour. This information came to their knowledge only after all the passengers were seated.</p>
            <p>Normally they would’ve informed the passengers directly but they decided to do something else. They told the passengers that the flight was going to be delayed by 4 hours. This really upset the passengers but there was nothing that they could do.</p>
            <p>After about 5 minutes, the pilots announced that their flight had received priority and was now only going to be delayed by 1 hour. Hearing this, the passengers were very happy and at the end of the flight, they thanked the crew and pilots for the flight and even left good reviews.

              I hope my point is clear here.
              
              The only thing to remember is that the outer box should contain a gift that is weirder than the gift itself.
              </p>
            <h3 class="sugghead">Check out some of our Suggestions-</h3>
            <h2 class="mb-3 mt-5">#2. A hamper of goodies</h2>
            <p>This might not be the best gift idea but it is good enough. For this you must know what the other person likes. If he/she is a coffee lover, then a set of coffee mug, a bottle of coffee and some related items packed together will work wonders.</p>
            <p>For bookworms, getting a new book Is not necessary. They will appreciate the fact that you got them something they love. It might be a little helpful though to know about the genre of books they read to be on the safer side.Just remember to get this packed in a nice, shiny wrapping paper. This will be the best surprise gift for your gf (girlfriend). Just plan it accordingly.</p>

            <h3 class="sugghead">Check out some of our Suggestions-</h3>
            <h2 class="mb-3 mt-5">#3. A real surprise gift: </h2>
            <p>For this, I want you to give a gift to the person such that they have no idea who gave it to them. On Christmas, I received a gift from Santa Claus (not handwritten, but printed). Isn't that cool? Even though I found out who gave the gift (Santa Claus obviously!), I felt that this format could be replicated in numerous ways.</p>
            <p>I believe that this is the true definition of a surprise gift (Unless you start giving obvious hints which will pin you down) . You might disclose the fact of the gift after a week because let’s be honest, we all crave attention.</p>
            <h3 class="sugghead">Check out some of our Suggestions-</h3>
            <h2 class="mb-3 mt-5">#4. Simple birthday surprise ideas:</h2>
            <p>Some of us are minimalists. We love it when our gift is not too high-fi but something worth remembering. If you are looking for a simple birthday surprise ideas for your best friend, then this might be it.</p>
            <p>Like described in the first paragraph, classic surprises work to this day, that's why they're termed classics. A getaway while going on a simple drive, or maybe a visit to a cafe they've been dying to go. You know best what to do. Surprises take time to be understood, make it wild enough so that they're not able to comprehend it for a while.</p>
            <h3 class="sugghead">Check out some of our Suggestions-</h3>
            <h2 class="mb-3 mt-5">#5. Opposite of point 1.</h2>
            <p>For point 1 we gave a gift which was bad based on the cover while the contents were good. Here we are going to be attempting to do the exact opposite. So we will be giving something that has a good box but a very bad content. 
              Eg. Give a shoe box which contains a coffee mug.
              </p>
            <p>This can be considered a prank and not a surprise. For this to work, you should have a clear record of not pranking. If you're a prank guru, well not pranking is the way to surprise more.</p>
            <p>I think that this will be perfect if you are looking for surprise gifts for birthday parties. It will entertain the audience too, when the gift is opened and will give everyone a chance to laugh.</p>
            <p>If you always give gifts that are genuinely good, the person will not see this coming. Both this and point 1 act as a compound surprise gift. How? The fact that you gave a gift in the first place will surprise the receiver. Then the box will set certain expectations for them. Now upon finding out that the box does not contain what it was meant to contain, they will be surprised again. This is why I call this the perfect surprise gift for anyone.</p>
            <h3 class="sugghead">Check out some of our Suggestions-</h3>
            <h2 class="mb-3 mt-5">#6. Just use your imagination:</h2>
            <p>Put yourself in the other person’s place. What kind of surprise would you love? Now plan to give the other person a surprise accordingly.</p>
            <p>PS: The previous point was introduced to make my job easier. : )</p>
            <h3 class="sugghead">Check out some of our Suggestions-</h3>
            <h2 class="mb-3 mt-5">#7. Beverages</h2>
            <p>Hear us out, I’m not talking about the sodas, unless well the person loves soda. If they adore tea or coffee, you have a chance to blow their minds away with the gift. Get away from the readily available brands, with social media on the rise and cafes opening up everywhere, finding the underrated and exceptional beverages is pretty easy. Grab a mixed collection, and gift them with a pleasant card. Boom there is your awesome gift. </p>
            <h3 class="sugghead">Check out some of our Suggestions-</h3>
            <h2 class="mb-3 mt-5">#8. Sneakers</h2>
            <p>When someone comes across an awesome sneaker, you can’t help but want it. All the more, cool sneakers are all memorable. A major tip, you can buy a clean, plain sneaker and later customize it to your liking, if you excel in the artistic area. If you blindly look for the well-known brands, you’ll be bankrupt. A little research is essential. Low tops or vans are the apt choices, but hey you can choose the freaky options as well.</p>
            <p>I think you can count this as best birthday gift for girlfriend under 1000 if you look at enough places.</p>
            <h3 class="sugghead">Check out some of our Suggestions-</h3>
            <h2 class="mb-3 mt-5">#9. Musical Instruments</h2>
            <p>For this gift you might have to spend some more. Ukuleles is one such instrument that comes to mind, if the person does like acoustic music or even lo-fi music. Harmonica is an alternative though it might not feel right, but it’s a beautiful instrument. If you really want to, you can gift an acoustic guitar as well.</p>
            <h3 class="sugghead">Check out some of our Suggestions-</h3>
            <h2 class="mb-3 mt-5">#10. Gift Basket</h2>
            <p>It might sound childish, but think of it as a gift that has all your comfort food, mixed with the small things that makes you happy. That is the essence of a gift basket. Just pour in all the essential things that make them giddy. Seeing them brighten is all you actually want, isn’t it?</p>
